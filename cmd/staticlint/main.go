// staticlint используется для статического анализа кода приложения
//
// Как использовать:
// из корня проекта выполнить команду go run cmd/staticlint/main.go ./...
// либо собрать staticlint и вызвать ее в корне проекта - staticlint ./...
//
// Используемые анализаторы:
// 	- golang.org/x/tools/go/analysis/passes/ - все анализаторы пакета
//  - staticcheck - все проверки, связанные с корректностью кода
//  - stylecheck - все проверки, связанные со стилистическими проблемами
//  - simple - все проверки, связанные с упрощением кода
//  - quickfix - содержит проверки, которые используются как часть gopls для автоматического рефакторинга
//  - ineffassign - обнаружение неэффективных присваиваний в коде
//  - bodyclose - проверяет, правильно ли закрыто res.Body
//  - osexitcheck - проверяет, отсутствие прямого вызова os.Exit в функции main пакета main

